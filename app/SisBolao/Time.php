<?php

namespace App\SisBolao;

use App\Time as TimeModel;
use Illuminate\Http\UploadedFile;
use Illuminate\Support\Facades\Storage;

class Time extends TimeModel
{

  /**
   * Time constructor.
   * @param array $attributes
   */
  public function __construct(array $attributes = [])
  {
    parent::__construct($attributes);
    $this->fillFields($attributes);
  }

  /**
   * Função que define todos os atributos da classe
   * @param array $attributes -
   * @return Time
   */
  public function fill(array $attributes)
  {
    $this->fillFields($attributes);
    parent::fill($attributes);
    return $this;
  }

  /**
   * @return mixed
   */
  public function getId()
  {
    return $this->id;
  }

  /**
   * @param mixed $id
   */
  public function setId($id)
  {
    $this->id = $id;
  }

  /**
   * @return mixed
   */
  public function getNome()
  {
    return $this->nome;
  }

  /**
   * @param mixed $nome
   */
  public function setNome($nome)
  {
    $this->nome = $nome;
  }

  /**
   * @return mixed
   */
  public function getAlias()
  {
    return $this->alias;
  }

  /**
   * @param mixed $alias
   */
  public function setAlias($alias)
  {
    $this->alias = $alias;
  }

  /**
   * @return mixed
   */
  public function getEscudo()
  {
    return $this->escudo;
  }

  /**
   * @param mixed $escudo
   */
  public function setEscudo(UploadedFile $escudo = null)
  {
    $this->escudo = $escudo;
  }

  /**
   * @param array $attributes
   */
  private function fillFields(array $attributes = [])
  {
    foreach ($attributes as $key => $value) {
      if (in_array($key, $this->fillable)) {
        $this->{$key} = $value;
      }
    }
  }

  /**
   * Salva o time em banco de dados;
   * @param array $options
   * @return bool
   */
  public function save(array $options = [])
  {
    $file = $this->getEscudo();
    if ($file !== null) {
      Storage::makeDirectory('times');
      $file_path = Storage::put('public/times', $file, 'public');
      $this->escudo = $file_path;
    }

    return parent::save($options); // TODO: Change the autogenerated stub
  }

}